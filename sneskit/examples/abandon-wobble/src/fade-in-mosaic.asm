;-------------------------------------------------------------------------;
.include "snes.inc"
.include "snes_zvars.inc"
;-------------------------------------------------------------------------;
.export FadeInMosaic
;-------------------------------------------------------------------------;


;/////////////////////////////////////////////////////////////////////////;
.code
;/////////////////////////////////////////////////////////////////////////;


;-------------------------------------------------------------------------;
	.a8
	.i16
;-------------------------------------------------------------------------;


;:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::;
FadeInMosaic:
;:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::;
	lda	m1
	lsr
	cmp	#16
	beq	endm

	sta	REG_INIDISP
	asl
	asl
	asl
	asl
	eor	#0f0h
	ora	#%111
	sta	REG_MOSAIC
	inc	m1

endm:	rts
